export function getCourseBySlug(slug: string): GolfCourse | undefined {
  return courses.find((c) => c.slug === slug);
}

export function getFilteredCourses(filters: {
  continent?: string;
  country?: string;
  difficulty?: string;
  maxGreenFee?: number;
  availability?: string;
}): GolfCourse[] {
  return courses.filter((course) => {
    if (filters.continent && course.continent !== filters.continent) return false;
    if (filters.country && course.country !== filters.country) return false;
    if (filters.difficulty && course.difficulty !== filters.difficulty) return false;
    if (filters.maxGreenFee && course.greenFee.min > filters.maxGreenFee) return false;
    if (filters.availability && course.availability !== filters.availability) return false;
    return true;
  });
}

